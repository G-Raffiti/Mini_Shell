#include "../../incs/error.h"

void	inv_quotes(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == QUOTE || str[i] == S_QUOTE)
			str[i] = str[i] * -1;
		i++;
	}
}
