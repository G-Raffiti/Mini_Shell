#include "../incs/libft.h"

int	ft_isalpha(char *str)
{
	while (*str)
	{
		if (!(*str >= 'a' && *str <= 'z') && !(*str >= 'A' && *str <= 'Z'))
			return (FALSE);
		str++;
	}
	return (TRUE);
}
